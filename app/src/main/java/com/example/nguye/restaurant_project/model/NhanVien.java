package com.example.nguye.restaurant_project.model;

import java.io.Serializable;


public class NhanVien implements Serializable {
    int maNV, CMND, maQuyen;
    String tenDN, matKhau, gioiTinh,ngaySinh;

    public NhanVien() {
    }

    public NhanVien(int maNV, int CMND, int maQuyen, String tenDN, String matKhau, String gioiTinh, String ngaySinh) {
        this.maNV = maNV;
        this.CMND = CMND;
        this.maQuyen = maQuyen;
        this.tenDN = tenDN;
        this.matKhau = matKhau;
        this.gioiTinh = gioiTinh;
        this.ngaySinh = ngaySinh;
    }

    public int getMaNV() {
        return maNV;
    }

    public void setMaNV(int maNV) {
        this.maNV = maNV;
    }

    public int getCMND() {
        return CMND;
    }

    public void setCMND(int CMND) {
        this.CMND = CMND;
    }

    public int getMaQuyen() {
        return maQuyen;
    }

    public void setMaQuyen(int maQuyen) {
        this.maQuyen = maQuyen;
    }

    public String getTenDN() {
        return tenDN;
    }

    public void setTenDN(String tenDN) {
        this.tenDN = tenDN;
    }

    public String getMatKhau() {
        return matKhau;
    }

    public void setMatKhau(String matKhau) {
        this.matKhau = matKhau;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(String ngaySinh) {
        this.ngaySinh = ngaySinh;
    }
}

